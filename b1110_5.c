#include <stdio.h>

int main(void) {
	// 0은 false, 1은 true
	printf("AND: %08X\n", 0x9 & 0xA); // 00001001 & 00001010 = 00001000 = 00000008
	printf("OR: %08X\n", 0x9 | 0xA); // 00001001 | 00001010 = 00001011 = 0000000B
	printf("XOR: %08X\n", 0x9 ^ 0xA); // 00001001 | 00001010 = 00000011 = 00000003
	printf("NOT: %08X\n", ~0x9); // ~00001001 = 11110110 = FFFFFFF6
	printf("<<: %08X\n", 0x4 << 1); // 00000100 << 1 = 00001000 = 00000008
	printf(">>: %08X\n", 0x4 >> 1); // 00000100 >> 1 = 00000010 = 00000002

	int a = 32;
	a = ~a;
	printf("a = %d\n", a); // 32의 1의 보수 -33, 32 = 00100000, ~32 = 11011111, -32 = 10100000, 10100000 + 00000001 = 10100001 = -33
	a = a + 0x01;
	printf("a = %d\n", a); // 32의 2의 보수 -32, 32 = 00100000, ~32 = 11011111, -32 = 10100000

	return 0;
}