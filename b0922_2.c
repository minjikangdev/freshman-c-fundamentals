#include <stdio.h>

int main(void)
{
	printf("결과값 = %d\n", 2 + 3); // 결과값 = 5
	printf("결과값 = %d\n", 2 - 3); // 결과값 = -1
	printf("결과값 = %d\n", 2 * 3); // 결과값 = 6
	printf("결과값 = %d\n", 2 / 3); // 결과값 = 0.666667 <- 0
	printf("결과값 = %f\n", 2 / 3); // 결과값 = 0.666667 <- 0.000000
	printf("결과값 = %f\n", 2 / 3.); // 결과값 = 0.666667
	printf("결과값 = %d\n", 2. / 3); // 결과값 = 0.666667 <- 1431655765
	return 0;
}